public class logic {
    public static void main(String[] args) {
        boolean x = true;
        boolean y = false;

        // Logical operations
        System.out.println("x = " + x + ", y = " + y);
        System.out.println("x && y: " + (x && y)); // Logical AND
        System.out.println("x || y: " + (x || y)); // Logical OR
        System.out.println("!x: " + (!x));         // Logical NOT
    }
}
