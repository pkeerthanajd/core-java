public class assignment 
{
    public static void main(String[] args) {
        int a = 10;
        int b;

        // Assignment operations
        b = a;
        System.out.println("b = a: " + b);

        b += 5;
        System.out.println("b += 5: " + b);

        b -= 3;
        System.out.println("b -= 3: " + b);

        b *= 2;
        System.out.println("b *= 2: " + b);

        b /= 4;
        System.out.println("b /= 4: " + b);

        b %= 3;
        System.out.println("b %= 3: " + b);
    }

}
